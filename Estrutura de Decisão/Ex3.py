#Faça um Programa que verifique se uma letra digitada é "F" ou "M". Conforme a letra escrever: F - Feminino, M - Masculino, Sexo Inválido.
Sexo = input("Digite seu sexo F - M:")

if(Sexo == "F"):
    print("F-Feminino")
elif(Sexo == "M"):
    print("M-Masculino")
else:
    print("Sexo inválido")    